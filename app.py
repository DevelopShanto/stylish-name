    res = []
    # ১০০টি ইউনিক স্টাইল জেনারেট করা
    for i in range(100):
        # ইংরেজি ডেকোরেশন
        eng_styled = eng_decors[i % len(eng_decors)].format(name)
        
        # আরবি স্টাইল (প্রথম ২০টি)
