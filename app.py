    # আরবির জন্য স্টাইলিশ ক্যালিগ্রাফি ম্যাপ
    arb_styles = [
        "آ{}آ", "ب{}ب", "ت{}ت", "ث{}ث", "ج{}ج", "ح{}ح", "خ{}خ", "د{}د", "ذ{}ذ", "ر{}ر",
        "ز{}ز", "س{}س", "ش{}ش", "ص{}ص", "ض{}ض", "ط{}ط", "ظ{}ظ", "ع{}ع", "غ{}غ", "ف{}ف"
    ]
    
