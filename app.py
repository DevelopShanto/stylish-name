from flask import Flask, render_template, request

app = Flask(__name__)

def generate_styles(name):
    # ১০০+ প্রিমিয়াম এবং ইমোজি ছাড়া ইংরেজি ডেকোরেশন
    eng_decors = [
        "꧁{}꧂", "亗 {} 亗", "『{}』", "★{}★", "× {} ×", "【{}】", "『ST』{}", "V I P {}", "Ｏ Ｎ Ｅ {}", "乃丹刀 {}",
        "『SH』{}", "⦇{}⦈", "⦉{}⦊", "⟬{}⟭", "⟮{}⟯", "『{}』", "「{}」", "〔{}〕", "⟨{}⟩", "[[ {} ]]",
        "«{}»", "‹{}›", "「{}」", "『{}』", "〖{}〗", "【{}】", "〔{}〕", "⟨{}⟩", "〚{}〛", "« {} »",
        "⌞{}⌟", "⌜{}⌝", "⸢{}⸣", "⸤{}⸥", "⦗{}⦘", "⦘{}⦗", "⟦{}⟧", "⟨{}⟩", "⟪{}⟫", "⟬{}⟭",
        "⁅{}⁆", "⁚{}⁚", "⁛{}⁛", "⁜{}⁜", "⁝{}⁝", "⁞{}⁞", "⁯{}⁯", "⁫{}⁫", "⁬{}⁬", "⁭{}⁭",
        "༺{}༻", "༼{}༽", "༽{}༼", "⁌{}⁍", "⁊{}⁋", "⸔{}⸕", "⸖{}⸗", "⸘{}⸙", "⸚{}⸛", "⸜{}⸝",
        "⸞{}⸟", "⸠{}⸡", "⸢{}⸣", "⸤{}⸥", "⸦{}⸧", "⸨{}⸩", "⸪{}⸫", "⸬{}⸭", "⸮{}?", "⸰{}⸱",
        "⸲{}⸳", "⸴{}⸵", "⸶{}⸷", "⸸{}⸹", "⸺{}⸻", "⸼{}⸽", "⸾{}⸿", "⹀{}⹁", "⹂{}⹃", "⹄{}⹅",
        "⹆{}⹇", "⹈{}⹉", "⹊{}⹋", "⹌{}⹍", "⹎{}⹏", "⹐{}⹑", "⹒{}⹓", "⹔{}⹕", "⹖{}⹗", "⹘{}⹙",
        "⹚{}⹛", "⹜{}⹝", "⹞{}⹟", "⹠{}⹡", "⹢{}⹣", "⹤{}⹥", "⹦{}⹧", "⹨{}⹩", "⹪{}⹫", "⹬{}⹭"
    ]
    
    # আরবির জন্য অরিগ্রাফিক ফন্ট ম্যাপ
    arb_map = {'a':'آ','b':'ب','c':'ث','d':'د','e':'ى','f':'ف','g':'ج','h':'ح','i':'ي','j':'ج','k':'ك','l':'ل','m':'م','n':'ن','o':'و','p':'پ','q':'ق','r':'ر','s':'س','t':'ت','u':'ع','v':'ڤ','w':'و','x':'خ','y':'ي','z':'ز'}
    
    # আরবির জন্য প্রিমিয়াম বর্ডার (ইমোজি ছাড়া)
    arb_borders = ["༺ {} ༻", "⦗ {} ⦘", "『 {} 』", "「 {} 」", "〔 {} 〕", "« {} »", "〖 {} 〗", "【 {} 】", "⟨ {} ⟩", "⟪ {} ⟫"]

    res = []
    for i in range(100):
        # ইংরেজি ডেকোরেশন (ইমোজি ছাড়া)
        eng_styled = eng_decors[i % len(eng_decors)].format(name)
        
        # আরবি স্টাইল (প্রথম ২০টি)
        arb_styled = ""
        if i < 20:
            pure_arb = "".join([arb_map.get(c.lower(), c) for c in name])
            arb_styled = arb_borders[i % len(arb_borders)].format(pure_arb)
            
        res.append({"eng": eng_styled, "arb": arb_styled})
    return res

@app.route("/", methods=["GET", "POST"])
def index():
    styles = []
    if request.method == "POST":
        name = request.form.get("name")
        styles = generate_styles(name)
    return render_template("index.html", styles=styles, tiktok="@shanto2993", salem="As-salamu Alaykum")
